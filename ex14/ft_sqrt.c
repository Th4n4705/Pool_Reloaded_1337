int		ft_sqrt(int nb)
{
	int		i;

	i = 1;
	if (nb == 0 || nb == 1)
		return (nb);
	if (nb > 0)
	{
		while (nb / 2 > i)
		{
			if (i * i == nb)
				return (i);
			i++;
		}
	}
	return (0);
}
